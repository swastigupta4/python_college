# defining different basic data types in python

# integer data type
age = 25  
print("Age:", age)

# float data type
height = 5.9  
print("Height:", height)

# string data type
name = "Alice"  
print("Name:", name)

# boolean data type
is_student = True  
print("Is student:", is_student)

# list data type (a collection of items)
fruits = ["apple", "banana", "cherry"]  
print("Fruits:", fruits)

# tuple data type (like a list but immutable)
coordinates = (10.5, 20.3)  
print("Coordinates:", coordinates)

# dictionary data type (key-value pairs)
person = {"name": "Alice", "age": 25, "city": "New York"}  
print("Person details:", person)

# set data type (collection of unique items)
unique_numbers = {1, 2, 3, 4, 5, 1, 2}  
print("Unique numbers:", unique_numbers)

# none data type (represents null value)
nothing = None  
print("Nothing:", nothing)
